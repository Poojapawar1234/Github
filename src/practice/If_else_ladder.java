package practice;

public class If_else_ladder {
public static void main(String[] args) {
	int marks=70;
	if(marks>=80) {
		System.out.println("distinction");
	}
	else if(marks>=60) {
	System.out.println("1st class");
}
else if(marks>=50) {
	System.out.println("2nd class");
}
else {
	System.out.println("fail");
}
}
}